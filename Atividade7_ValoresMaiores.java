package package1;
import java.util.*;
public class Atividade7_ValoresMaiores {
public static void main(String[] args) {
		
	Scanner sc = new Scanner(System.in);
	System.out.println("Insira um valor númerico:");
	double valNumero = sc.nextDouble();
	
	if (valNumero >= 10) {
		System.out.println("O valor inserido é maior ou igual a 10!");	
	} else {
		System.out.println("O valor inserido é menor que 10!");

  }
 }
}